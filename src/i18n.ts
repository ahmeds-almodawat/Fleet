function applyDirection(lang: string) {
  const isArabic = lang?.toLowerCase().startsWith('ar');
  document.documentElement.dir = isArabic ? 'rtl' : 'ltr';
  document.documentElement.lang = isArabic ? 'ar' : 'en';
}

